#include "utils/mat3x3.h"

float	mat3x3_det(t_mat3x3 a)
{
	return ((a._[0][0] * (a._[1][1] * a._[2][2] - a._[1][2] * a._[2][1]))
			+ (a._[0][1] * (a._[1][2] * a._[2][0] - a._[1][0] * a._[2][2]))
			+ (a._[0][2] * (a._[1][0] * a._[2][1] - a._[1][1] * a._[2][0])));
}

t_mat3x3	mat3x3_adj(t_mat3x3 a)
{
	t_mat3x3	tmp;

	tmp._[0][0] = (a._[1][1] * a._[2][2] - a._[1][2] * a._[2][1]);
	tmp._[0][1] = -(a._[0][1] * a._[2][2] - a._[0][2] * a._[2][1]);
	tmp._[0][2] = (a._[0][1] * a._[1][2] - a._[0][2] * a._[1][1]);
	tmp._[1][0] = -(a._[1][0] * a._[2][2] - a._[1][2] * a._[2][0]);
	tmp._[1][1] = (a._[0][0] * a._[2][2] - a._[0][2] * a._[2][0]);
	tmp._[1][2] = -(a._[0][0] * a._[1][2] - a._[0][2] * a._[1][0]);
	tmp._[2][0] = (a._[1][0] * a._[2][1] - a._[1][1] * a._[2][0]);
	tmp._[2][1] = -(a._[0][0] * a._[2][1] - a._[0][1] * a._[2][0]);
	tmp._[2][2] = (a._[0][0] * a._[1][1] - a._[0][1] * a._[1][0]);
	return (tmp);
}

t_mat3x3	mat3x3_inv(t_mat3x3 a)
{
	return (mat3x3_mul_scalar(1 / mat3x3_det(a), mat3x3_adj(a)));
}

t_mat3x3	mat3x3_transpose(t_mat3x3 a)
{
	float	tmp;

	tmp = a._[0][1];
	a._[0][1] = a._[1][0];
	a._[1][0] = tmp;
	tmp = a._[0][2];
	a._[0][2] = a._[2][0];
	a._[2][0] = tmp;
	tmp = a._[1][2];
	a._[1][2] = a._[2][1];
	a._[2][1] = tmp;
	return (a);
}
